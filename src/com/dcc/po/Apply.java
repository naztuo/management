package com.dcc.po;

public class Apply {

    private String apply_id;

    private String apply_name;

    private int apply_phone;

    private String apply_content;

    private int apply_time;

    private int status;

    public String getApply_id() {
        return apply_id;
    }

    public void setApply_id(String apply_id) {
        this.apply_id = apply_id;
    }

    public String getApply_name() {
        return apply_name;
    }

    public void setApply_name(String apply_name) {
        this.apply_name = apply_name;
    }

    public int getApply_phone() {
        return apply_phone;
    }

    public void setApply_phone(int apply_phone) {
        this.apply_phone = apply_phone;
    }

    public String getApply_content() {
        return apply_content;
    }

    public void setApply_content(String apply_content) {
        this.apply_content = apply_content;
    }

    public int getApply_time() {
        return apply_time;
    }

    public void setApply_time(int apply_time) {
        this.apply_time = apply_time;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
